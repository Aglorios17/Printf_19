#include "../include/ft_printf.h"

char	*ft_strcpy2(char *dest, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		dest[i] = c;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
